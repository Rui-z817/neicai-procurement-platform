export const infoPrices: InfoPrice[] = [];
let iid = 0;
const now = new Date();
const curY = now.getFullYear();
const curM = now.getMonth() + 1;
regions.forEach((region, rIdx) => {
  // 每个地区最近6个月的信息价
  for (let back = 0; back < 6; back++) {
    let m = curM - back;
    let y = curY;
    if (m <= 0) {
      m += 12;
      y -= 1;
    }
    // 北京/上海多一些类型，其他地区2-3种
    const typeCount = region === "北京" ? 5 : region === "上海" ? 4 : region === "广东" ? 3 : (rIdx % 2 === 0 ? 2 : 1);
    infoTypes.slice(0, typeCount).forEach((it) => {
      iid += 1;
      const title = `${region}${y}年${String(m).padStart(2, "0")}月${it.suffix ? it.suffix : ""}信息价`;
      infoPrices.push({
        id: `ip${iid}`,
        region,
        year: y,
        month: m,
        type: it.type,
        title,
        publishDate: `${y}-${String(m).padStart(2, "0")}-15`,
      });
    });
  }
});

